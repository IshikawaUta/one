#variable
angka1 = 10
angka2 = 5
angka3 = 1

#operasi penjumlahan
hasil_penjumlahan = angka1 + angka2 + angka3
print("Hasil penjumlahan:", hasil_penjumlahan)

#operasi pengurangan
hasil_pengurangan = angka1 - angka2 - angka3
print("Hasil penjumlahan:", hasil_pengurangan)

#operasi perkaklian
hasil_perkalian = angka1 * angka2 * angka3
print("Hasil penjumlahan:", hasil_perkalian)

#operasi pembagian
hasil_pembagian = angka1 / angka2 / angka3
print("Hasil penjumlahan:", hasil_pembagian)

#operasi pemangkatan
hasil_pemangkatan = angka1 ** angka2 ** angka3
print("Hasil penjumlahan:", hasil_pemangkatan)